function ProjectCard({ project }: { project: any }) {
  const getHealthColor = (score: number) => {
    if (score > 80) return 'text-green-400 bg-green-400/10 border-green-400/20';
    if (score > 50) return 'text-yellow-400 bg-yellow-400/10 border-yellow-400/20';
    return 'text-red-400 bg-red-400/10 border-red-400/20';
  };

  const healthScore = project.latestAudit ? 85 : 45;

  return (
    <Link href={`/projects/${project.id}`} className="block">
      <div className="glass-card rounded-xl p-6 group hover:border-primary/30 transition-colors cursor-pointer flex flex-col gap-6 relative overflow-hidden h-full">
        {/* Decorative background blob */}
        <div className="absolute -top-10 -right-10 w-32 h-32 bg-primary/5 rounded-full blur-3xl group-hover:bg-primary/10 transition-colors pointer-events-none" />

        <div className="flex items-start justify-between relative z-10 min-w-0">
          <div className="min-w-0 flex-1 pr-3">
            <h3 className="text-lg font-bold tracking-tight text-white group-hover:text-primary transition-colors truncate" title={project.name}>
              {project.name}
            </h3>
            <p className="text-xs text-muted-foreground truncate" title={project.domain}>{project.domain}</p>
          </div>
          <div className={`w-12 h-12 rounded-full border flex items-center justify-center font-bold text-sm shrink-0 ${getHealthColor(healthScore)}`}>
            {healthScore}
          </div>
        </div>

        <div className="flex items-center gap-4 text-xs text-muted-foreground relative z-10">
          <div className="flex items-center gap-1.5 shrink-0">
            <Activity className="w-4 h-4 text-primary" />
            <span>{project.latestAudit?.status === 'completed' ? 'Auditado' : 'Pendiente'}</span>
          </div>
          <div className="w-1 h-1 rounded-full bg-border" />
          <div className="flex items-center gap-1.5 shrink-0">
            <Globe className="w-4 h-4 text-accent" />
            <span>{project.integrations?.length || 0} Integraciones</span>
          </div>
        </div>

        <div className="pt-4 border-t border-border/50 flex items-center justify-between relative z-10">
          <span className="text-[10px] text-muted-foreground">
            Actualizado {new Date(project.updatedAt).toLocaleDateString()}
          </span>
          <span className="text-xs text-primary hover:text-primary/80 font-semibold flex items-center gap-1 transition-colors">
            Ver Detalles <ChevronRight className="w-3.5 h-3.5" />
          </span>
        </div>
      </div>
    </Link>
  );
}
